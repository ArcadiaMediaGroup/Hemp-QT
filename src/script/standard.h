class CNoDestination {
public:
    friend bool operator==(const CNoDestination &a, const CNoDestination &b) { (void)b; (void)a; return true; }
    friend bool operator<(const CNoDestination &a, const CNoDestination &b) { (void)b; (void)a; return true; }
};

struct WitnessV0ScriptHash : public uint256 {};
